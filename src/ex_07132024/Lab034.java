package ex_07132024;

public class Lab034 {
    public static void main(String[] args) {
        //Compound assignment operators
        int a = 24;
        // +=, -=, /=, *=, %=
        //+= ->
        //a = a+ 24
        a += 24; // a= a+24
        a -= 24; // a= a-24
        a /= 24; // a= a/24
        a *= 24; // a= a*24

    }
}
